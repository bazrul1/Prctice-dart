void main  ()  {
  // for loop with switch case
  for (int i = 3; i<=5; i++ ) {
    print ('Day $i');
    
    //switch and case semiler to if else
    switch (i) {
      case 1:
      print ('Monday');
      break;
      case 2:
      print ('Wednesday');
      break;
      case 3:
      print ('Tuesday');
      break;
      case 4:
      print ('Thursday');
      break;
      case 5:
      print ('Friday');
      break;
      case 6:
      print ('Saturday');
      break;
      case 7:
      print ('Sunday');
      break;
      
      default :
      print ('invelid day');


    }

  }

}
